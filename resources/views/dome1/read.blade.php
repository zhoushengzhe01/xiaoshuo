					孟浩刚一临近，阁楼大门无声无息的打开，从内走出一个身子干瘦的中年男子，这男子穿着深绿色长袍，眉目间有精明之感。他右手抬起一抓，立刻孟浩身前的玉简飞入此人手中，被他看了一眼后，懒洋洋的传出话语。<br />
					<br />
					“孟浩，晋升外宗弟子，赐你单独屋舍，绿袍、灵牌、储物袋，持灵牌可入宝阁获取一样法宝。”精芒男子右手一甩，立刻一个灰色的口袋落在孟浩手中。<br />
					<br />
					孟浩看着手中灰色口袋一愣，想起了这一路上看到的所有外宗弟子，腰上都挂着这样的口袋。<br />
					<br />
					“灵气入内，可装若干物。”精明男子看了孟浩一眼，内心立刻判断出对方必定没有什么外宗熟人，否则不可能连储物袋都不会使用，内心略有放心，淡淡开口。<br />
					<br />
					孟浩闻言，立刻将体内不多的灵气运转涌入口袋内，眼前一阵模糊，仿佛看到了一处半人大小的空间，里面有绿袍玉简等物。<br />
					<br />
					看到这一幕，他顿时有些兴奋，暗道这储物袋怕是也价值百金，这样的宝物可算是仙家手段。<br />
					<br />
					心念动时，空间内一枚玉简出现在他的手里，凝神一看，那里面是一副地图，描述了在这外宗区域内，一处偏僻角落的屋舍，那里以后将属于孟浩。<br />
					<br />
					“回去再看，宝阁已经打开，还不入内。”精明男子冷声说道。<br />
					<br />
					孟浩抬起头，将储物袋放入怀里后，看了眼敞开的阁楼大门，深吸口气，带着一丝期待，迈步踏入其内。<br />
					<br />
					在踏入宝阁的一瞬，孟浩忽然神色一变，倒吸口气。<br />
